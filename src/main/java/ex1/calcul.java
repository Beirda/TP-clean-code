package ex1;

public class calcul {

    /**
     * méthode permettant d'addition les paramètres a et b mis en arguments
     * @param a
     * @param b
     * @return l'addition de a et b
     */
    public int addition(int a, int b) {
        return a + b;
    }

    /**
     * méthode permettant de soustraire le paramètre b du paramètre a mis en arguments
     * @param a
     * @param b
     * @return la soustraction de a et b
     */
    public int soustraction(int a, int b) {
        return a - b;
    }
}
